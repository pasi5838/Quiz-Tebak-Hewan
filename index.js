let currentLevel = 0;
let score = 0;
let lives = 3;

function updateLivesDisplay() {
  const heartContainer = document.getElementById("lives");
  heartContainer.innerHTML = "❤️".repeat(lives);
}

function loadLevel() {
  const question = quizData[currentLevel];
  document.getElementById("animalImage").src = question.image;

  const optionsContainer = document.getElementById("optionsContainer");
  optionsContainer.innerHTML = "";

  question.options.sort(() => Math.random() - 0.5);

  question.options.forEach(option => {
    const button = document.createElement("button");
    button.textContent = option;
    button.onclick = () => checkAnswer(option);
    optionsContainer.appendChild(button);
  });

  document.getElementById("score").textContent = `Level: ${currentLevel + 1} / ${quizData.length}`;
  updateLivesDisplay();
}

function checkAnswer(selected) {
  const correct = quizData[currentLevel].name;
  if (selected === correct) {
    alert("Benar!");
    score++;
  } else {
    alert(`Salah. Jawaban yang benar adalah: ${correct}`);
    lives--;
    if (lives <= 0) {
      document.getElementById("animalImage").style.display = "none";
      document.getElementById("optionsContainer").style.display = "none";
      document.getElementById("score").textContent = `Permainan berakhir! Skor akhir: ${score} dari ${quizData.length}`;
      document.getElementById("lives").textContent = "❤️❤️❤️";
      return;
    }
  }

  currentLevel++;
  if (currentLevel < quizData.length) {
    loadLevel();
  } else {
    document.getElementById("animalImage").style.display = "none";
    document.getElementById("optionsContainer").style.display = "none";
    document.getElementById("score").textContent = `Selamat! Kamu telah menyelesaikan semua level. Skor akhir: ${score} dari ${quizData.length}`;
  }
}

loadLevel();
